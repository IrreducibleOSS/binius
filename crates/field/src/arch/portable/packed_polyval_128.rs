pub type PackedBinaryPolyval1x128b = PackedPrimitiveType<u128, BinaryField128bPolyval>;

// Define broadcast
impl_broadcast!(u128, BinaryField128bPolyval);

// Define multiply
impl Mul for PackedBinaryPolyval1x128b {
	type Output = Self;

	fn mul(self, rhs: Self) -> Self::Output {
		crate::tracing::trace_multiplication!(PackedBinaryPolyval1x128b);

		montgomery_multiply(self.0, rhs.0).into()
	}
}

// Define square
impl_square_with!(PackedBinaryPolyval1x128b @ ReuseMultiplyStrategy);

// Define invert
impl InvertOrZero for PackedBinaryPolyval1x128b {
	fn invert_or_zero(self) -> Self {
		// The algorithm is ported from https://github.com/reyzin/GF2t/blob/master/src/main/gf2t/GF2_128.java#L400
		// The only change is the way how x^(2^(2^k)) is calculated.
		// Computes self^{2^128-2}
		// self * self^(2^128 - 2) = self ^ (2^128-1) = 1 if self != 0

		// Contains self raised to the power whose binary representation is 2^k ones
		let mut self_pow_2_pow_k1s = self.get(0);

		// Square res to get its exponent to be 10 in binary
		let mut res = pow_2_2_n(self_pow_2_pow_k1s, 0);

		// Contains sel raised to the power whose binary representation is 2^k ones followed by 2^k zeros
		let mut selt_pow_2_pow_k1s_to_k0s = res;

		// Loop invariant
		// res contains z raised to the power whose binary representation is 2^{k+1}-1 ones followed by a single zero
		// self_pow_2_pow_k1s contains z raised to the power whose binary representation is 2^k ones
		// selt_pow_2_pow_k1s_to_k0s contains z raised to the power whose binary representation is 2^k ones followed by 2^k zeros
		for k in 1..7 {
			// Fill in the zeros in the exponent of selt_pow_2_pow_k1s_to_k0s with ones
			self_pow_2_pow_k1s *= selt_pow_2_pow_k1s_to_k0s;

			// selt_pow_2_pow_k1s_to_k0s = pow_2_2_n with 2^k zeros appended to the exponent
			selt_pow_2_pow_k1s_to_k0s = pow_2_2_n(self_pow_2_pow_k1s, k);

			// prepend 2^k ones to res
			res *= selt_pow_2_pow_k1s_to_k0s;
		}

		Self::set_single(res)
	}
}

